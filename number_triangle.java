public class number_triangle {
    public static void main(String[] args) {
        square(8);

    }
    public static void square(int n){
        for (int i = 1; i <=n ; i++) {
            System.out.println(" ");
            for (int j = 1; j <=i ; j++) {
                System.out.print(j);

            }

        }
    }
}
